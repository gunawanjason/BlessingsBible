// Helper functions
export function getTranslationLanguage(translationId) {
  return BOOK_TRANSLATIONS[translationId] || "en";
}

export function getBookName(englishName, translationId) {
  const language = getTranslationLanguage(translationId);
  return BOOK_NAMES[language]?.[englishName] || englishName;
}

export function getTestamentLabel(testament, translationId) {
  const language = getTranslationLanguage(translationId);
  return (
    TESTAMENT_LABELS[language]?.[testament] || TESTAMENT_LABELS.en[testament]
  );
}

// Reverse lookup - get English name from translated name
export function getEnglishBookName(translatedName, translationId) {
  const language = getTranslationLanguage(translationId);
  if (language === "en") return translatedName;

  const bookNames = BOOK_NAMES[language];
  if (!bookNames) return translatedName;

  // Find the English key for this translated value (case insensitive)
  const lowerTranslatedName = translatedName.toLowerCase();
  for (const [englishName, localName] of Object.entries(bookNames)) {
    if (localName.toLowerCase() === lowerTranslatedName) {
      return englishName;
    }
  }

  return translatedName;
}

// Get all book names in a specific language with search capability
export function getBookNamesForSearch(translationId) {
  const language = getTranslationLanguage(translationId);
  const bookNames = BOOK_NAMES[language];

  if (!bookNames) return [];

  return Object.entries(bookNames).map(([englishName, localName]) => ({
    englishName,
    localName,
    searchTerms: [localName.toLowerCase(), englishName.toLowerCase()],
  }));
}
